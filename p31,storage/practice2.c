#include<stdio.h>
int main()
{
	char a = -128;
	//char中只能存放1个字节
	//10000000000000000000000010000000原码
	//11111111111111111111111101111111反码
	//11111111111111111111111110000000补码
	//10000000
	//整形提升为 11111111111111111111111110000000补码 因为是打印无符号整形所以不需要求原码
	//整形提升时按有符号还是无符号看得是自身类型 而不是看打印的类型；
	printf("%u\n", a);
	return 0;
}